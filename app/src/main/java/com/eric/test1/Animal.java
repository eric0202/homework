package com.eric.test1;

public class Animal {
    public String name;
    public int img;

    public Animal(String name, int img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }


}
